let handler = async (m, { conn }) => {
if (!m.quoted) return m.reply("‚ö†Ô∏è *Silakan reply gambar, video, atau audio yang ingin dijadikan View Once!*")
let mime = m.quoted.mimetype || ""
if (!/image|video|audio/.test(mime)) return m.reply("‚ö†Ô∏è *Hanya bisa mengubah gambar, video, atau audio menjadi View Once!*")
try {
let media = await m.quoted.download()
if (!media) return m.reply("‚ùå *Gagal mengunduh media!*")
let options = {
caption: m.quoted.text || "",
viewOnce: true
}
if (/image/.test(mime)) {
await conn.sendMessage(m.chat, { image: media, ...options }, { quoted: m })
} else if (/video/.test(mime)) {
await conn.sendMessage(m.chat, { video: media, ...options }, { quoted: m })
} else if (/audio/.test(mime)) {
await conn.sendMessage(m.chat, { audio: media, mimetype: "audio/ogg", ptt: true, viewOnce: true }, { quoted: m })
}
} catch (e) {
console.error(e)
m.reply("‚ùå *Gagal mengubah media menjadi View Once!*")
}
}

handler.help = ["sendviewonce"]
handler.tags = ["tools"]
handler.command = /^send(view(once)?)?|svo$/i
handler.register = true
handler.premium = true

export default handler