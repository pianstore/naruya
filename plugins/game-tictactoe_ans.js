import { format } from 'util'

let debugMode = !1

let winScore = 4999
let playScore = 99

export async function before(m) {
let ok
let isWin = !1
let isTie = !1
let isSurrender = !1
if (m.isBaileys || m.fromMe) return
conn.tictactoe = conn.tictactoe ? conn.tictactoe : {}
let room = Object.values(this.tictactoe).find(room => room.id && room.game && room.state && room.id.startsWith('tictactoe') && [room.game.playerX, room.game.playerO].includes(m.sender) && room.state == 'PLAYING')
if (room) {
let setting = global.db.data.settings[conn.user.jid]
if (setting.composing)
await this.sendPresenceUpdate('composing', m.chat)
if (setting.autoread)
await this.readMessages([m.key])
// m.reply(`[DEBUG]\n${parseInt(m.text)}`)
if (!/^([1-9]|(me)?nyerah|surr?ender)$/i.test(m.text))
return !0
isSurrender = !/^[1-9]$/.test(m.text)
if (m.sender !== room.game.currentTurn) { // nek wayahku
if (!isSurrender)
return !0
}
if (debugMode)
m.reply('[DEBUG]\n' + require('util').format({
isSurrender,
text: m.text
}))
if (!isSurrender && 1 > (ok = room.game.turn(m.sender === room.game.playerO, parseInt(m.text) - 1))) {
m.reply({
'-3': 'Game telah berakhir',
'-2': 'Invalid',
'-1': 'Posisi Invalid',
0: 'Posisi Invalid',
}[ok])
return !0
}
if (m.sender === room.game.winner)
isWin = true
else if (room.game.board === 511)
isTie = true
let arr = room.game.render().map(v => {
return {
X: '‚ùå',
O: '‚≠ï',
1: '1Ô∏è‚É£',
2: '2Ô∏è‚É£',
3: '3Ô∏è‚É£',
4: '4Ô∏è‚É£',
5: '5Ô∏è‚É£',
6: '6Ô∏è‚É£',
7: '7Ô∏è‚É£',
8: '8Ô∏è‚É£',
9: '9Ô∏è‚É£',
}[v]
})
if (isSurrender) {
room.game._currentTurn = m.sender === room.game.playerX
isWin = true
}
let winner = isSurrender ? room.game.currentTurn : room.game.winner
let str = `
${arr.slice(0, 3).join('')}
${arr.slice(3, 6).join('')}
${arr.slice(6).join('')}
${isWin ? `@${winner.split('@')[0]} Menang! (+${winScore} XP)` : isTie ? `Game berakhir (+${playScore} XP)` : `Giliran ${['‚ùå', '‚≠ï'][1 * room.game._currentTurn]} (@${room.game.currentTurn.split('@')[0]})`}
‚ùå: @${room.game.playerX.split('@')[0]}
‚≠ï: @${room.game.playerO.split('@')[0]}
Ketik *nyerah* untuk nyerah
Room ID: ${room.id}
`.trim()
let users = global.db.data.users
if ((room.game._currentTurn ^ isSurrender ? room.x : room.o) !== m.chat)
room[room.game._currentTurn ^ isSurrender ? 'x' : 'o'] = m.chat
if (room.x !== room.o)
await this.reply(room.x, str, m, { mentions: this.parseMention(str) })
await this.reply(room.o, str, m, { mentions: this.parseMention(str) })
if (isTie || isWin) {
users[room.game.playerX].exp += playScore
users[room.game.playerO].exp += playScore
if (isWin)
users[winner].exp += winScore - playScore
if (debugMode)
m.reply('[DEBUG]\n' + format(room))
delete this.tictactoe[room.id]
}
}
return !0
}