import pkg from "baileys"

const { generateWAMessageFromContent, proto, prepareWAMessageMedia } = pkg

let handler = async (m, { conn, text }) => {
if (!text) return m.reply("üîç *Masukkan kata kunci untuk mencari gambar di Pixiv!*")
try {
await global.loading(m, conn)
const apiUrl = global.API("lol", "/api/pixiv", { query: text }, "apikey")
const response = await fetch(apiUrl)
if (!response.ok) throw new Error(`API Error: ${response.status} - ${response.statusText}`)
const json = await response.json()
if (!json.result || json.result.length === 0) return m.reply("üçÇ *Tidak ada hasil ditemukan di Pixiv!*")
const images = json.result.slice(0, 10)
let slides = []

for (let item of images) {
let media = await prepareWAMessageMedia({ image: { url: item.image } }, { upload: conn.waUploadToServer })
slides.push({
header: proto.Message.InteractiveMessage.Header.fromObject({
hasMediaAttachment: true,
...media
}),
body: { text: item.title },
nativeFlowMessage: proto.Message.InteractiveMessage.NativeFlowMessage.fromObject({
buttons: [{
name: "cta_url",
buttonParamsJson: `{\"display_text\":\"Buka di Pixiv\",\"url\":\"${item.image}\"}`
}]
})
})
}

const msg = await generateWAMessageFromContent(m.chat, {
viewOnceMessageV2Extension: {
message: {
messageContextInfo: {
deviceListMetadata: {},
deviceListMetadataVersion: 2
},
interactiveMessage: proto.Message.InteractiveMessage.fromObject({
body: { text: `‚ú® *Hasil Gambar Pixiv: ${text}*` },
carouselMessage: { cards: slides }
})
}
}}, { userJid: m.sender, quoted: m })

await conn.relayMessage(m.chat, msg.message, { messageId: msg.key.id })

} catch (e) {
console.error(e)
m.reply("‚ùå *Gagal mengambil data dari Pixiv. Coba lagi nanti!*")
} finally {
await global.loading(m, conn, true)
}
}

handler.help = ["pixiv"]
handler.tags = ["search"]
handler.command = /^(pixiv)$/i
handler.premium = true
handler.register = true

export default handler