import pkg from "baileys"
const { generateWAMessageFromContent, proto, prepareWAMessageMedia } = pkg

let handler = async (m, { conn, text }) => {
if (!text) return m.reply("ğŸ” *Masukkan kata kunci untuk mencari wallpaper!*")
try {
await global.loading(m, conn)
let res = await fetch(`https://api.hiuraa.my.id/search/wallpaper?q=${text}`)
let json = await res.json()
if (!json.result || json.result.length === 0) return m.reply("ğŸ–¼ï¸ *Wallpaper tidak ditemukan!*")
let results = json.result.slice(0, 10)
let slides = []
for (let item of results) {
let thumb = item.image?.[0] || ''
let img = await prepareWAMessageMedia({ image: { url: thumb } }, { upload: conn.waUploadToServer })
slides.push({
header: proto.Message.InteractiveMessage.Header.fromObject({
hasMediaAttachment: true,
...img
}),
nativeFlowMessage: proto.Message.InteractiveMessage.NativeFlowMessage.fromObject({
buttons: [{
name: "cta_url",
buttonParamsJson: `{\"display_text\":\"ğŸŒ Sumber: ${item.type}\",\"url\":\"${item.source}\",\"merchant_url\":\"${item.source}\"}`
}]
})
})
}
let msg = await generateWAMessageFromContent(m.chat, {
viewOnceMessageV2Extension: {
message: {
messageContextInfo: {
deviceListMetadata: {},
deviceListMetadataVersion: 2
},
interactiveMessage: proto.Message.InteractiveMessage.fromObject({
body: { text: `ğŸ–¼ï¸ *Hasil Wallpaper: ${text}*` },
carouselMessage: { cards: slides }
})
}
}
}, { userJid: m.sender, quoted: m })
await conn.relayMessage(m.chat, msg.message, { messageId: msg.key.id })
} catch (e) {
console.error(e)
m.reply("ğŸ¥€ *Ups, terjadi kesalahan saat mencari wallpaper!*")
} finally {
await global.loading(m, conn, true)
}
}

handler.help = ["wallpaper"]
handler.tags = ["search"]
handler.command = /^(wallpaper|wp)$/i
handler.limit = true
handler.register = true

export default handler